void delay_200ms(void)
{
	uint32_t m,j,k;
	m = j = k = 500000;
	// this will introduce ~200ms delay when system clock is 16MHz
	for (uint32_t i = 0; i < 500000 / 5; i++)
	{
		m--;
		j--;
		k--;
	}
}

int main(void)
{
	gpio_TC_002();
	
	while(1)
	{
		LVQ_GPIO_TogglePinValue(GPIOC, GPIO_PIN_13);
		delay_200ms();
	}
}

